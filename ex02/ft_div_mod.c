#include <stdio.h>
void	ft_swap(int *a, int *b)
{
	int aux;

	aux = *a;
	*a = *b;
	*b = aux;
}

int	main(void)
{
	int a;
	int b;

	a = 15;
	b = 9;
	
	ft_swap(&a, &b);
	
	printf("%d", a);
}